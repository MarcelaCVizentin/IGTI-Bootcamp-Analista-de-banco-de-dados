SELECT C.NOM_CLIENTE AS "NOME DO CLIENTE", P.NUM_PEDIDO AS PEDIDO,
P.DATA_PEDIDO AS "DATA DO PEDIDO",
P.VLR_TOTAL_PEDIDO
FROM CLIENTE C JOIN PEDIDO P
ON C.COD_CLIENTE = P.COD_CLIENTE
WHERE DATA_PEDIDO > '2020'
ORDER BY [NOME DO CLIENTE]

