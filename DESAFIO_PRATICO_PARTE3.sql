GO

--NOM_FORNECEDOR, NUM_TELEFONE, DSC_SERVICO, TIP_SERVICO e VLR_SERVICO.
--alfabeticamente pelo nome do fornecedor e descendentemente pelo valor do serviço.
SELECT F.NOM_FORNECEDOR AS FORNECEDOR,
F.NUM_TELEFONE AS TELEFONE,
S.DSC_SERVICO AS DESCRICAO_SERVICO,
S.TIP_SERVICO AS TIPO, VLR_SERVICO
FROM FORNECEDOR F LEFT JOIN SERVICO S
ON F.COD_FORNECERDOR = S.COD_FORNECEDOR
ORDER BY FORNECEDOR ASC, VLR_SERVICO DESC 
GO

